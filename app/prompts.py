NO_ANSWER_RU = "В предоставленных документах нет информации по данному вопросу."
NO_ANSWER_KK = "Ұсынылған құжаттарда бұл сұрақ бойынша ақпарат жоқ."


# ============================================================================
# Registry в variants — для /ab/test и динамической смены
# ============================================================================

PROMPT_VARIANTS = ("strict", "balanced", "concise")


def get_system_prompt(language: str, variant: str = "strict") -> str:
    """
    Возвращает системный промпт для заданного языка и варианта.
    Если variant неизвестен — fallback на 'strict'.
    """
    if variant not in PROMPT_VARIANTS:
        variant = "strict"
    if language == "kk":
        # few-shot ВПЕРЕДИ, чтобы маркер «ЖАУАП:» оставался последним —
        # иначе модель продолжает примеры вместо ответа (протечка few-shot)
        prompt = KAZAKH_FEW_SHOT + _PROMPTS_KK[variant]
    else:
        prompt = _PROMPTS_RU[variant]
    return prompt


def get_no_answer(language: str) -> str:
    """Возвращает шаблон no-answer для заданного языка."""
    if language == "kk":
        return NO_ANSWER_KK
    return NO_ANSWER_RU


def list_variants() -> dict:
    """Для /ab/test endpoint: показывает доступные варианты промптов."""
    return {
        "variants": list(PROMPT_VARIANTS),
        "default": "strict",
        "description": {
            "strict": "Максимально строгий, минимум галлюцинаций (по умолчанию)",
            "balanced": "Баланс между строгостью и полнотой ответа",
            "concise": "Краткий ответ (только факты + цитаты)",
        },
    }
